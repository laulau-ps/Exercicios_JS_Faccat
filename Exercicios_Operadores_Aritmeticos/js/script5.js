let baseMenor = parseInt(prompt("Digite a menor base do trapézio: "))
let baseMaior = parseInt(prompt("Digite a maior base do trapézio: "))
let altura = parseInt(prompt("Digite a altura do trapézio: "))
let area = (baseMenor + baseMaior) * altura / 2
alert("A área do seu trapézio é de: " + area + "cm²")